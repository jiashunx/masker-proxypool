package com.jiashunx.app.masker.proxypool.core.exception;

/**
 * 初始化异常
 * @author jiashunx
 */
public class MProxyInitializeException extends MProxyException {

    public MProxyInitializeException() {
        super();
    }

    public MProxyInitializeException(String message) {
        super(message);
    }

    public MProxyInitializeException(String message, Throwable throwable) {
        super(message, throwable);
    }

    public MProxyInitializeException(Throwable throwable) {
        super(throwable);
    }

}
