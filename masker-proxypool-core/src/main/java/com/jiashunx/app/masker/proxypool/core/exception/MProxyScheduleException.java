package com.jiashunx.app.masker.proxypool.core.exception;

/**
 * 调度异常.
 * @author jiashunx
 */
public class MProxyScheduleException extends MProxyException {

    public MProxyScheduleException() {
        super();
    }

    public MProxyScheduleException(String message) {
        super(message);
    }

    public MProxyScheduleException(String message, Throwable throwable) {
        super(message, throwable);
    }

    public MProxyScheduleException(Throwable throwable) {
        super(throwable);
    }

}
